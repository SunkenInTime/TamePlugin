package com.hypixel.hytale.server.core.asset.type.blocktype.config;

import com.hypixel.hytale.assetstore.AssetRegistry;
import com.hypixel.hytale.assetstore.AssetExtraInfo.Data;
import com.hypixel.hytale.assetstore.codec.AssetBuilderCodec;
import com.hypixel.hytale.assetstore.codec.AssetBuilderCodec.Builder;
import com.hypixel.hytale.assetstore.map.DefaultAssetMap;
import com.hypixel.hytale.assetstore.map.JsonAssetWithMap;
import com.hypixel.hytale.codec.Codec;
import com.hypixel.hytale.codec.KeyedCodec;
import com.hypixel.hytale.codec.codecs.StringIntegerCodec;
import com.hypixel.hytale.codec.codecs.map.MapCodec;
import java.util.Collections;
import java.util.HashMap;
import java.util.Map;
import javax.annotation.Nonnull;

public class BlockMigration implements JsonAssetWithMap<Integer, DefaultAssetMap<Integer, BlockMigration>> {
   public static final AssetBuilderCodec<Integer, BlockMigration> CODEC;
   private static DefaultAssetMap<Integer, BlockMigration> ASSET_MAP;
   protected Data data;
   protected int id;
   protected Map<String, String> directMigrations = Collections.emptyMap();
   protected Map<String, String> nameMigrations = Collections.emptyMap();

   public static DefaultAssetMap<Integer, BlockMigration> getAssetMap() {
      if (ASSET_MAP == null) {
         ASSET_MAP = (DefaultAssetMap)AssetRegistry.getAssetStore(BlockMigration.class).getAssetMap();
      }

      return ASSET_MAP;
   }

   public BlockMigration(int id, Map<String, String> directMigrations, Map<String, String> nameMigrations) {
      this.id = id;
      this.directMigrations = directMigrations;
      this.nameMigrations = nameMigrations;
   }

   protected BlockMigration() {
   }

   @Nonnull
   public Integer getId() {
      return this.id;
   }

   @Nonnull
   public String getMigration(@Nonnull String blockTypeKey) {
      String direct = (String)this.directMigrations.get(blockTypeKey);
      if (direct != null) {
         return direct;
      } else {
         String name = (String)this.nameMigrations.get(blockTypeKey);
         return name != null ? name : blockTypeKey;
      }
   }

   public String getDirectMigration(String blockTypeKey) {
      return (String)this.directMigrations.getOrDefault(blockTypeKey, blockTypeKey);
   }

   public String getNameMigration(@Nonnull String blockTypeKey) {
      return (String)this.nameMigrations.getOrDefault(blockTypeKey, blockTypeKey);
   }

   public Map<String, String> getDirectMigrations() {
      return this.directMigrations;
   }

   public Map<String, String> getNameMigrations() {
      return this.nameMigrations;
   }

   @Nonnull
   public String toString() {
      int var10000 = this.id;
      return "BlockMigration{id='" + var10000 + "', directMigrations=" + String.valueOf(this.directMigrations) + ", nameMigrations=" + String.valueOf(this.nameMigrations) + "}";
   }

   static {
      CODEC = ((Builder)((Builder)AssetBuilderCodec.builder(BlockMigration.class, BlockMigration::new, new StringIntegerCodec(), (blockMigration, i) -> {
         blockMigration.id = i;
      }, (blockMigration) -> {
         return blockMigration.id;
      }, (asset, data) -> {
         asset.data = data;
      }, (asset) -> {
         return asset.data;
      }).addField(new KeyedCodec("DirectMigrations", new MapCodec(Codec.STRING, HashMap::new)), (blockMigration, document) -> {
         blockMigration.directMigrations = document;
      }, (blockMigration) -> {
         return blockMigration.directMigrations;
      })).addField(new KeyedCodec("NameMigrations", new MapCodec(Codec.STRING, HashMap::new)), (blockMigration, document) -> {
         blockMigration.nameMigrations = document;
      }, (blockMigration) -> {
         return blockMigration.nameMigrations;
      })).build();
   }
}
